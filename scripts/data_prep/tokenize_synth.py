#!/usr/bin/env python3
"""
tokenize_synth.py

Reads JSONL data generated by `generate_evq_synth.py`, tokenizes the text field,
and packs it into a high-performance `int64` standard numpy memmap file for fast
loading on Blackwell GPUs (avoids CPU int32 -> int64 casting overhead during train loop).

Requirements:
- transformers
- numpy
- tqdm
"""

from __future__ import annotations

import argparse
import json
import struct
from pathlib import Path
from typing import Dict, Iterable, List

import numpy as np
from tqdm import tqdm
from transformers import AutoTokenizer


def num_lines_in_file(path: Path) -> int:
    """Fast line counting for progress bar."""
    count = 0
    with path.open("r", encoding="utf-8") as f:
        for _ in f:
            count += 1
    return count


def process_jsonl(
    jsonl_path: Path,
    tokenizer: AutoTokenizer,
    output_bin: Path,
    output_meta: Path,
    max_seq_len: int,
    pad_to_max: bool = False,
) -> None:
    """
    Reads jsonl_path, tokenizes each 'text' field.
    If pad_to_max is True, pads/truncates to max_seq_len.
    Writes a continuous stream of int64 token IDs to output_bin.
    """
    total_lines = num_lines_in_file(jsonl_path)
    print(f"[info] Tokenizing {jsonl_path} ({total_lines} lines)")

    total_tokens = 0
    skipped_docs = 0

    # Ensure output directory exists
    output_bin.parent.mkdir(parents=True, exist_ok=True)

    # We will write raw binary int64. 
    # Alternatively we could use np.memmap, but sequential writing is often easiest via plain file writes.
    with jsonl_path.open("r", encoding="utf-8") as fin:
        with output_bin.open("wb") as fout:
            for line in tqdm(fin, total=total_lines, desc="Tokenizing"):
                line = line.strip()
                if not line:
                    continue
                try:
                    obj = json.loads(line)
                    text = obj["text"]
                except Exception:
                    skipped_docs += 1
                    continue

                # Add special tokens if your model expects it (like BOS). 
                # generate_evq_synth.py appends answer, so usually add_special_tokens=True is fine.
                tokens = tokenizer.encode(text, add_special_tokens=True)

                if pad_to_max:
                    if len(tokens) > max_seq_len:
                        tokens = tokens[:max_seq_len]
                    elif len(tokens) < max_seq_len:
                        tokens = tokens + [tokenizer.pad_token_id or tokenizer.eos_token_id] * (max_seq_len - len(tokens))

                # Convert to int64 numpy array and write raw bytes
                arr = np.array(tokens, dtype=np.int64)
                fout.write(arr.tobytes())
                total_tokens += len(arr)

    # Save metadata so DataLoader knows shape
    meta = {
        "source_jsonl": str(jsonl_path),
        "total_documents": total_lines - skipped_docs,
        "total_tokens": total_tokens,
        "dtype": "int64",
        "pad_to_max": pad_to_max,
        "max_seq_len": max_seq_len if pad_to_max else None,
        "vocab_size": len(tokenizer),
    }

    output_meta.write_text(json.dumps(meta, indent=2))
    print(f"[ok] Wrote {total_tokens} tokens to {output_bin}")
    print(f"[ok] Wrote metadata to {output_meta}")


def main() -> None:
    parser = argparse.ArgumentParser(description="Tokenize synthetic EVQ jsonl into int64 binary files.")
    parser.add_argument("--input-dir", type=Path, required=True, help="Directory containing train.jsonl, val.jsonl")
    parser.add_argument("--output-dir", type=Path, required=True, help="Directory to save .bin and meta.json")
    parser.add_argument("--tokenizer-path", type=str, required=True, help="Path to HF tokenizer")
    parser.add_argument("--max-seq-len", type=int, default=32768, help="Max sequence length")
    parser.add_argument("--pad-to-max", action="store_true", help="If set, pad all sequences to --max-seq-len")

    args = parser.parse_args()

    # Load tokenizer
    print(f"[info] Loading tokenizer from {args.tokenizer_path}")
    # Use fast tokenizer and disable padding warnings
    tokenizer = AutoTokenizer.from_pretrained(args.tokenizer_path, use_fast=True)
    if tokenizer.pad_token_id is None:
        tokenizer.pad_token_id = tokenizer.eos_token_id

    args.output_dir.mkdir(parents=True, exist_ok=True)

    for split in ["train", "val", "test"]:
        in_file = args.input_dir / f"{split}.jsonl"
        if not in_file.exists():
            continue
        
        out_bin = args.output_dir / f"{split}.bin"
        out_meta = args.output_dir / f"{split}_meta.json"

        process_jsonl(
            jsonl_path=in_file,
            tokenizer=tokenizer,
            output_bin=out_bin,
            output_meta=out_meta,
            max_seq_len=args.max_seq_len,
            pad_to_max=args.pad_to_max,
        )


if __name__ == "__main__":
    main()
